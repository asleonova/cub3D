#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t len;
	size_t i;

	len = 0;
	if (dest == NULL)
		return (0);
	while (src[len])
		len++;
	i = 0;
	if (!size)
		return (len);
	while (src[i] && i < (size - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (len);
}
