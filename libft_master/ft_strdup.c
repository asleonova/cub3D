#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	size_t	len;
	char	*copy;

	i = 0;
	len = ft_strlen((char*)str);
	if (!(copy = (char*)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (str[i])
	{
		copy[i] = str[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
