#include "libft.h"

/*
** Checks if a character is whitespace.
*/

int	ft_iswspace(int c)
{
	return (c == '\t' ||
			c == '\n' ||
			c == '\v' ||
			c == '\f' ||
			c == '\r' ||
			c == ' ');
}
